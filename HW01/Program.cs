int GetDegree(int numberA, int numberB)
{
    int degree = 0;

    for (int i = 0; i <= numberA && i <= numberB; i++)
    {
        degree = Convert.ToInt32(Math.Pow(numberA, numberB));
    }

    return degree;
}

